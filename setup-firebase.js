#!/usr/bin/env node
/**
 * Script para configurar Firebase con variables de entorno
 * WhitePearl Translations - Sistema de Asignaciones
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

// Colores para la consola
const colors = {
    reset: '\x1b[0m',
    bright: '\x1b[1m',
    dim: '\x1b[2m',
    red: '\x1b[31m',
    green: '\x1b[32m',
    yellow: '\x1b[33m',
    blue: '\x1b[34m',
    magenta: '\x1b[35m',
    cyan: '\x1b[36m',
    white: '\x1b[37m'
};

// Funci√≥n para imprimir con colores
const print = (text, color = 'white') => {
    console.log(colors[color] + text + colors.reset);
};

// Interfaz de readline
const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

// Funci√≥n para hacer preguntas
const askQuestion = (question) => {
    return new Promise((resolve) => {
        rl.question(question, (answer) => {
            resolve(answer.trim());
        });
    });
};

// Funci√≥n para validar input requerido
const getRequiredInput = async (prompt, defaultValue = '') => {
    let input = '';
    do {
        if (defaultValue) {
            input = await askQuestion(`${prompt} [${defaultValue}]: `);
            if (!input) input = defaultValue;
        } else {
            input = await askQuestion(`${prompt}: `);
        }
        
        if (!input) {
            print('‚ö†Ô∏è  Este campo es obligatorio. Por favor, ingresa un valor.', 'yellow');
        }
    } while (!input);
    
    return input;
};

// Funci√≥n principal
const setupFirebase = async () => {
    try {
        print('üî• Configurador de Firebase para WhitePearl Translations', 'cyan');
        print('=========================================================', 'cyan');
        print('');

        print('üìã Ingresa los datos de tu proyecto Firebase:', 'green');
        print('   Puedes encontrar estos datos en: Firebase Console > Project Settings > General > Your apps > Web app', 'dim');
        print('');

        // Solicitar datos de Firebase
        const firebaseConfig = {
            apiKey: await getRequiredInput('üîë API Key'),
            authDomain: await getRequiredInput('üåê Auth Domain (ej: tu-proyecto.firebaseapp.com)'),
            databaseURL: await getRequiredInput('üóÑÔ∏è  Database URL (ej: https://tu-proyecto-default-rtdb.firebaseio.com/)'),
            projectId: await getRequiredInput('üìÇ Project ID'),
            storageBucket: await getRequiredInput('üì¶ Storage Bucket (ej: tu-proyecto.appspot.com)'),
            messagingSenderId: await getRequiredInput('üì® Messaging Sender ID'),
            appId: await getRequiredInput('üÜî App ID')
        };

        // Crear contenido del archivo .env
        const envContent = `# Firebase Configuration - WhitePearl Translations
# Generado autom√°ticamente el ${new Date().toLocaleString('es-ES')}

# ‚ö†Ô∏è IMPORTANTE: No compartas este archivo p√∫blicamente
# Agrega .env a tu .gitignore si usas Git

# Firebase Project Configuration
REACT_APP_FIREBASE_API_KEY=${firebaseConfig.apiKey}
REACT_APP_FIREBASE_AUTH_DOMAIN=${firebaseConfig.authDomain}
REACT_APP_FIREBASE_DATABASE_URL=${firebaseConfig.databaseURL}
REACT_APP_FIREBASE_PROJECT_ID=${firebaseConfig.projectId}
REACT_APP_FIREBASE_STORAGE_BUCKET=${firebaseConfig.storageBucket}
REACT_APP_FIREBASE_MESSAGING_SENDER_ID=${firebaseConfig.messagingSenderId}
REACT_APP_FIREBASE_APP_ID=${firebaseConfig.appId}

# Opcional: URL base de la aplicaci√≥n (para links compartidos)
REACT_APP_BASE_URL=http://localhost:3000
`;

        // Crear archivo .env
        const envPath = path.join(process.cwd(), '.env');
        fs.writeFileSync(envPath, envContent, 'utf8');
        print('‚úÖ Archivo .env creado exitosamente', 'green');

        // Contenido actualizado para firebase.js
        const firebaseJsContent = `import { initializeApp } from 'firebase/app';
import { getDatabase } from 'firebase/database';
import { getAuth } from 'firebase/auth';

// Configuraci√≥n de Firebase usando variables de entorno
// Las variables deben estar definidas en el archivo .env
const firebaseConfig = {
  apiKey: process.env.REACT_APP_FIREBASE_API_KEY,
  authDomain: process.env.REACT_APP_FIREBASE_AUTH_DOMAIN,
  databaseURL: process.env.REACT_APP_FIREBASE_DATABASE_URL,
  projectId: process.env.REACT_APP_FIREBASE_PROJECT_ID,
  storageBucket: process.env.REACT_APP_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.REACT_APP_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.REACT_APP_FIREBASE_APP_ID
};

// Validar que todas las variables est√©n configuradas
const requiredEnvVars = [
  'REACT_APP_FIREBASE_API_KEY',
  'REACT_APP_FIREBASE_AUTH_DOMAIN',
  'REACT_APP_FIREBASE_DATABASE_URL',
  'REACT_APP_FIREBASE_PROJECT_ID',
  'REACT_APP_FIREBASE_STORAGE_BUCKET',
  'REACT_APP_FIREBASE_MESSAGING_SENDER_ID',
  'REACT_APP_FIREBASE_APP_ID'
];

const missingVars = requiredEnvVars.filter(varName => !process.env[varName]);

if (missingVars.length > 0) {
  console.error('üî• Error de configuraci√≥n de Firebase:');
  console.error('Las siguientes variables de entorno no est√°n configuradas:');
  missingVars.forEach(varName => console.error('- ' + varName));
  console.error('Por favor, verifica tu archivo .env');
  throw new Error('Configuraci√≥n de Firebase incompleta');
}

// Inicializar Firebase
const app = initializeApp(firebaseConfig);

// Inicializar servicios de Firebase
export const db = getDatabase(app);  // Realtime Database
export const auth = getAuth(app);

// Exportar configuraci√≥n para debugging (solo en desarrollo)
if (process.env.NODE_ENV === 'development') {
  console.log('üî• Firebase configurado correctamente:', {
    projectId: firebaseConfig.projectId,
    authDomain: firebaseConfig.authDomain,
    databaseURL: firebaseConfig.databaseURL
  });
}

export default app;
`;

        // Actualizar firebase.js
        const firebaseJsPath = path.join(process.cwd(), 'src', 'services', 'firebase.js');
        fs.writeFileSync(firebaseJsPath, firebaseJsContent, 'utf8');
        print('‚úÖ Archivo firebase.js actualizado para usar variables de entorno', 'green');

        // Crear .env.example
        const envExampleContent = `# Firebase Configuration - WhitePearl Translations
# Archivo de ejemplo - Copia este archivo como .env y completa con tus datos reales

# Firebase Project Configuration
REACT_APP_FIREBASE_API_KEY=tu-api-key-aqui
REACT_APP_FIREBASE_AUTH_DOMAIN=tu-proyecto.firebaseapp.com
REACT_APP_FIREBASE_DATABASE_URL=https://tu-proyecto-default-rtdb.firebaseio.com/
REACT_APP_FIREBASE_PROJECT_ID=tu-proyecto-id
REACT_APP_FIREBASE_STORAGE_BUCKET=tu-proyecto.appspot.com
REACT_APP_FIREBASE_MESSAGING_SENDER_ID=123456789
REACT_APP_FIREBASE_APP_ID=1:123456789:web:abcdef

# Opcional: URL base de la aplicaci√≥n
REACT_APP_BASE_URL=http://localhost:3000
`;

        const envExamplePath = path.join(process.cwd(), '.env.example');
        fs.writeFileSync(envExamplePath, envExampleContent, 'utf8');
        print('‚úÖ Archivo .env.example creado como referencia', 'green');

        // Gestionar .gitignore
        const gitignorePath = path.join(process.cwd(), '.gitignore');
        let gitignoreContent = '';

        if (fs.existsSync(gitignorePath)) {
            gitignoreContent = fs.readFileSync(gitignorePath, 'utf8');
            if (!gitignoreContent.includes('.env')) {
                print('üîí Agregando .env a .gitignore por seguridad...', 'yellow');
                fs.appendFileSync(gitignorePath, '\n# Environment variables\n.env\n');
                print('‚úÖ .env agregado a .gitignore', 'green');
            }
        } else {
            print('üìù Creando .gitignore con configuraci√≥n de seguridad...', 'yellow');
            const gitignoreTemplate = `# Dependencies
node_modules/
/.pnp
.pnp.js

# Testing
/coverage

# Production
/build

# Environment variables (IMPORTANT: Never commit these)
.env
.env.local
.env.development.local
.env.test.local
.env.production.local

# Logs
npm-debug.log*
yarn-debug.log*
yarn-error.log*

# IDE
.vscode/
.idea/
*.swp
*.swo

# OS
.DS_Store
Thumbs.db
`;
            fs.writeFileSync(gitignorePath, gitignoreTemplate, 'utf8');
            print('‚úÖ .gitignore creado con configuraciones de seguridad', 'green');
        }

        // Mostrar resumen
        print('', 'white');
        print('üéâ ¬°Configuraci√≥n completada exitosamente!', 'green');
        print('=========================================', 'green');
        print('', 'white');
        print('üìÅ Archivos creados/actualizados:', 'cyan');
        print('   ‚úì .env (configuraci√≥n privada)', 'white');
        print('   ‚úì .env.example (plantilla p√∫blica)', 'white');
        print('   ‚úì src/services/firebase.js (actualizado)', 'white');
        print('   ‚úì .gitignore (configuraci√≥n de seguridad)', 'white');
        print('', 'white');
        print('üîê Seguridad:', 'yellow');
        print('   ‚Ä¢ El archivo .env contiene informaci√≥n sensible', 'white');
        print('   ‚Ä¢ NO lo compartas p√∫blicamente o en repositorios', 'white');
        print('   ‚Ä¢ Ya est√° agregado a .gitignore para protegerlo', 'white');
        print('', 'white');
        print('üöÄ Pr√≥ximos pasos:', 'cyan');
        print('   1. Ejecuta: npm start', 'white');
        print('   2. Verifica que la aplicaci√≥n se conecte a Firebase', 'white');
        print('   3. Configura las reglas de Realtime Database en Firebase Console', 'white');
        print('', 'white');
        print('üìö Documentaci√≥n adicional en README.md', 'dim');
        print('', 'white');

        // Preguntar si desea ejecutar npm start
        const runNpm = await askQuestion('¬øDeseas ejecutar npm start ahora? (y/N): ');
        if (runNpm.toLowerCase() === 'y' || runNpm.toLowerCase() === 'yes') {
            print('', 'white');
            print('üöÄ Iniciando la aplicaci√≥n...', 'green');
            
            // Ejecutar npm start
            const { spawn } = require('child_process');
            const npmStart = spawn('npm', ['start'], {
                stdio: 'inherit',
                shell: true
            });

            npmStart.on('error', (error) => {
                print(`‚ùå Error al ejecutar npm start: ${error.message}`, 'red');
            });
        }

        print('', 'white');
        print('‚ú® ¬°Listo para usar WhitePearl Translations! ‚ú®', 'magenta');

    } catch (error) {
        print(`‚ùå Error durante la configuraci√≥n: ${error.message}`, 'red');
        process.exit(1);
    } finally {
        rl.close();
    }
};

// Ejecutar si se llama directamente
if (require.main === module) {
    setupFirebase();
}

module.exports = { setupFirebase };
